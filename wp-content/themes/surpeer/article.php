					<div class="grap" itemprop="articleBody">
						<p><img src="./article_files/webp"></p>
						<p>订阅《家居廊》快1年了，这几年一直是杂志的阅读者，前两年主要订阅的是《时尚先生》、《ELLEMEN》这类男装时尚杂志，后来因为住所不是很稳定，就只剩下《家居廊》这一本了。</p>
						<p>订阅《家居廊》最主要的目的是参考里面给出的设计，也了解下当下流行的高端品牌。内容里的消费显然不是我这个级别的，单品价格比时尚杂志里的要魔幻许多，一个桌子动不动就十万+的价格，真是吓得我赶紧合上杂志。</p>
						<p>不过抱着我只学习并不买的心态来看，内容还是很不错的。最喜欢的板块就是home，介绍全世界各地的家。当然，基本都是土豪的家，座标基本都是一线城市的核心区，上海的法租界、里昂的老区，要么就是阿尔卑斯山脚。虽然距离甚远，但确实开阔了眼界。</p>
						<p>和LV 合作的城市指南也很不错，是从另一个角度来欣赏一座城市，给了我很多不同的思路。和传统的景区打卡不同，城市指南更多的是介绍当地最好的酒店、美食、设计馆，最后才是地标。尤其是餐厅，在一座城市生活了很久，居然都不知道还有这种餐厅的存在，想来自己的消费层次实在是太低了。</p>
						<p><img src="./article_files/webp(1)"></p>
						<p>家居产品和其他产品最大的不同就是大部分产品都是可以使用很久，而且一些单品往往是越使用越有味道。一定程度上也影响了我的消费观。现在购物前最先思考的是这件东西能使用多久，几个月下来，发现自己很少购物了，但生活品质反而提高了不少。</p>
						<p>虽然现在互联网信息发达，但是觉得杂志还是一个非常不错的信息获取方式，比在网上瞎逛强多了，杂志内容一般都是精选，网上的大多都弄个广告什么的，讲道理，杂志上的广告也都是不错的。但是因为互联网的冲击，不少很不错的杂志都停刊了，比如我很喜欢的《CAPA咔啪》。现在离搬家又不远了，完成后打算订阅《旅行家》、《安邸》和《男人装》。</p>
